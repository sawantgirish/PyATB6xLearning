# take 3 inputs from user and perform  ADD, SUB, DIV , AND MULTIPLICATION

num1 = float(input("Enter a number: "))
num2 = float(input("Enter another number: "))
num3 = float(input("Enter a third number: "))

# perform operations add/sub/div/mul

add_num = num1 + num2 + num3
subtract_num = num1 - num2 - num3
divide_num = num1 / num2 / num3
multiply_num = num1 * num2 * num3
print(add_num)
print(subtract_num)
print(divide_num)
print(multiply_num)
